package pk4;

public class RelaTest_01 {

	public static void main(String[] args) {

		char a,b;
		a='A'; // 65
		b='B'; // 66
		
		System.out.println("a>b : " + (a>b));        // 65>66 :false
		System.out.println("a<b : " + (a<b));        // 65<66 :true
		System.out.println("a==b : " + (a==b));      // 65=66 :false
		System.out.println("a!=b : " + (a!=b));      // 65¡Á66 :false
		
	}

}
